    <h1>Glossary</h1>
    <p>A total of five different  measurements and calculated values were used to determine a numeric value for  each bulb and dimmer combination (including different dimmer  settings/curves/profiles available on some dimmer models). These bulb-dimmer interactions  included:</p>
    <ul>
      <li><strong>Linearity Score</strong>&ndash; as measured by correlation  coefficient R2 of the lamp lumen output when dimmed from 100 to 0 in  increments of 10% </li>
      <li><strong>Re-fire</strong> &ndash; after a lamp is dimmed to 0,  then dimmer setting is increased in increments of 1.0 until bulb produces human visible  light</li>
      <li><strong>Medium performance scale- (Projected vs Actual Lumen Output at 50% dimmer setting)
        </strong>
        <ul>
          <li>Accuracy  of lumen output at 50% = lumen output at 50% dimmer setting รท (MLO x 0.5) </li>
        </ul>
      </li>

      <li><strong>Low performance scale- (Projected vs Actual Lumen Output at 20% dimmer setting)
      </strong>
      <ul><li>Accuracy of lumen output at 20% = lumen output at 20% dimmer setting รท (MLO x 0.2)</li></ul></li>

      <li><strong>Lumen output efficiency- measured at 50%</strong> = lumens per watt at 50% dimmer setting</li>
      <li><strong>MLO =</strong>Maximum measured lumen output at 100% dimmer setting</li>
    </ul>
    <p>The ideal score would be 100, however no bulb-dimmer combination scored this high. Bulbs performed differently with different dimmers & with different profiles (curves) of the same dimmer. Dimmers can alter bulb light output & efficiencies.
</p>
